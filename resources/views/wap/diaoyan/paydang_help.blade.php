<div class="t-main1" style="margin-top:0.5em; padding:0 1em">
    <center><b style="line-height:2em; color:#b01523; font-size:1.2em">缴费说明</b></center>
    <b>计算基数</b>
    <p>凡有工资收入的党员，每月以国家规定的工资总额中相对固定的、经常性的工资收入为计算基数，按规定比例交纳党费。工资总额中相对固定的、经常性的工资收入包括：机关工作人员（不含工人）的职务工资、级别工资、基础工资、工龄工资、津贴；事业单位专业技术人员、管理人员的职务工资、等级工资、津贴、奖金；机关、事业单位工人的岗位工资、等级工资、津贴、奖金；企业人员工资收入中的固定部分（基本工资）和活的部分（津贴、奖金）。实行年薪制人员中的党员以上年月平均收入为计算基数。列入交纳党费计算基数的津贴、奖金，是指年功性津贴、地区性津贴、工资性津贴和按月发放的奖金。</p>
    <b>缴纳比例</b>
    <p>中共中央组织部下发《关于中国共产党党费收缴、使用和管理的规定》，对党费交纳比例作出调整，新的党费收缴标准自2008年4月1日起施行。</p>
    <p>新标准中党员交纳党费的比例为：</p>
    <p>每月工资收入(税后)在3000元以下(含3000元)者，交纳月工资收入的0.5%；</p>
    <p>3000元以上至5000元(含5000元)者，交纳1%；</p>
    <p>5000元以上至10000元(含10000元)者，交纳1.5%；</p>
    <p>10000元以上者，交纳2%。</p>
    <p>离退休干部、职工中的党员，每月以实际领取的离退休费总额或养老金总额为计算基数，5000元以下(含5000元)的按0.5%交纳党费，5000元以上的按1%交纳党费。</p>
    <p>同时，农民党员每月交纳党费0.2-1元。学生党员、下岗失业的党员、依靠抚恤或救济生活的党员、领取当地最低生活保障金的党员，每月交纳党费0.2元。新标准于2008年4月1日起施行。</p>
